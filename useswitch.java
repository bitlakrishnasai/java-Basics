public class useswitch {public static void main(String args[]){
    int a=5;
    switch (a){
        case 1:
            System.out.println("1");
            break;
        case 2:
            System.out.println("2");
            break;
        case 3:
            System.out.println("4");
            break;
        case 5:
            System.out.println("5");
            break;
        default:
            System.out.println("default");
    }

}
}
